function Get-ConstantNode([string]$name) {
  $xml.SelectSingleNode("//*[local-name()='constant' and @name='$name']")
}

function Set-ConstantValue([string]$name, [string]$value) {
  $node = Get-ConstantNode $name
  if (-not $node) { throw "Missing constant: $name" }
  $node.InnerText = $value
}

function Format-Matrix($rows, [int]$digits = 1) {
  $fmt = '{0:0.' + ('0' * $digits) + '}'
  $lines = foreach ($row in $rows) {
    '         ' + (($row | ForEach-Object { $fmt -f [double]$_ }) -join ' ') + ' '
  }
  "`n" + ($lines -join "`n") + "`n      "
}

